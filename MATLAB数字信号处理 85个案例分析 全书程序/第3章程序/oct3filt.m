function [B,A] = oct3filt(f0,Fs,N)

oc6=2^(1/6);                 % 按式(3-16-4)先求出2^(1/6)
fl=f0/oc6;                   % 按式(3-16-4)由中心频率求出低截止频率
fu=f0*oc6;                   % 按式(3-16-4)由中心频率求出高截止频率
fs2=Fs/2;                    % 为归一化采样频率取一半
wl=fl/fs2;                   % 计算归一化低截止频率
wu=fu/fs2;                   % 计算归一化高截止频率
[B,A]=butter(N,[wl wu]);     % 计算巴特沃斯滤波系数











